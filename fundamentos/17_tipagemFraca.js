/*
    JavaScript possui tipagem dinâmica (é fracamente tipada), ou seja, uma variável pode assumir diferentes tipos ao longo da execução do programa
    Em linguagens de tipagem estática ou fortemente tipadas, a variável possuirá somente o tipo recebido no momento da declaração

*/

let qualquer = 'Legal'
console.log(qualquer)
console.log(typeof qualquer)

qualquer = 9
console.log(qualquer)
console.log(typeof qualquer)

qualquer = false
console.log(qualquer)
console.log(typeof qualquer)

